export const draw_grid = function(scene,box_size)
{
    for(let j=0;j<grid[0].length;j++)
    {
        for(let i=0;i<grid.length;i++)
        {
            if(grid[i][j] === 0)
            {
                let box = new Quadrilateral(
                    [j*box_size,i*box_size],
                    [j*box_size,i*box_size+box_size],
                    [j*box_size+box_size,i*box_size+box_size],
                    [j*box_size+box_size,i*box_size],
                                        [0,0,1,1]
                )
                scene.add(box)
            }
            else{
                let box = new Quadrilateral(
                    [j*box_size,i*box_size],
                    [j*box_size,i*box_size+box_size],
                    [j*box_size+box_size,i*box_size+box_size],
                    [j*box_size+box_size,i*box_size],
                    [0,0,0,1]

                )
                scene.add(box)               
            }      

        }
    }
}